# Method 1: Simple print statement
print("Hello, Python!")

# Method 2: Using a variable
message = "Hello, Python!"
print(message)

# Method 3: Using string concatenation
print("Hello, " + "Python!")

# Method 4: Using f-strings (formatted string literals)
language = "Python"
print(f"Hello, {language}!")

# Method 5: Using the join method
words = ["Hello", "Python!"]
print(" ".join(words))




